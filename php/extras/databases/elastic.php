/**
* @describe returns pre-build queries based on name
* @param name string
*	[running_queries]
*	[table_locks]
* @return query string
*/
function elasticNamedQuery($name){
	switch(strtolower($name)){
		case 'tables':
			return <<<ENDOFQUERY
	SHOW tables
ENDOFQUERY;
		break;
		case 'functions':
			return <<<ENDOFQUERY
	SHOW functions
ENDOFQUERY;
		break;
		case 'catalogs':
			return <<<ENDOFQUERY
	SHOW catalogs
ENDOFQUERY;
		break;
	}
}
